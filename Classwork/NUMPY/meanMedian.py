# finding the Mean and Median of the array elements
import numpy as np

# creating array
arr=np.array([10,40,20,50,22,12,35,78])

# printing the array
print("Array:\n",arr)

# finding the mean value using the mean method
meanValue=np.mean(arr)

# printing the mean of array
print("Mean of the Array:",meanValue)

# finding the median of array using the median method
medianValue=np.median(arr)

# printing the median of array
print("Median of Array:",medianValue)






# given two arrays and we'll add them using numpy module

import numpy as np

# creating first array
arr1=np.array([1,2])
# creating second array
arr2=np.array([3,4])

# printing first array
print("First array:\n",arr1)
# printing Second array
print("Second array:\n",arr2)

# adding both arrays using add method of numpy
arr3=np.add(arr1,arr2)

# printing sum of arrays
print("Sum of arrays:\n",arr3)

